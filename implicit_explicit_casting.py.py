# Implicit Type Casting
# Declare an integer and float number
x = 5     # Integer
y = 2.2   # Float

# Adding integer and float number
z = x + y

# Print the result and its datatype
print("Result of implicit type casting (x + y):", z)
print("Datatype of z:", type(z))

# Explicit Type Casting
# Declare a float number
a = 5.10

# Converting a float to an integer
b = int(a)

# Print the result and its datatype
print("Result of explicit type casting (int(a)):", b)
print("Datatype of b:", type(b))
